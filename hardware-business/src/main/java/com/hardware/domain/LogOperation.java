package com.hardware.domain;

public class LogOperation {

	private String key;
	private String desc;
	
	public LogOperation(String key, String desc){
		this.key = key;
		this.desc = desc;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
